#include <bits/stdc++.h>
using namespace std;

void setIO(string name = "io") {
	ios_base::sync_with_stdio(false);cin.tie(nullptr);
	if (name.size()) {
		freopen((name + ".in").c_str(), "r", stdin);
		freopen((name + ".out").c_str(), "w", stdout);
	}
}

int main() {
    setIO();
    int t; cin >> t;
    return 0;
}